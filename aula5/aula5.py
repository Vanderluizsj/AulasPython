"""
+ soma
- subtração
* multiplicação
/ divisão
// divisão inteira
** exponenciação
% resto da divisão
() parênteses, usados para determinar precedência
"""
print(10+5)                      #soma - resultado=15
print("Luiz"+ " "+ "Gustavo")    #soma - resultado=Luiz Gustavo
print(10-5)                      #subtração - resultado=5
print(10/5)                      #divisão - resultado=2.0
print(10//5)                     #divisão inteira - resultado=2
print(3**2)                      #exponenciação - resultado=9
print(10%5)                      #resto da divisão - resultado=0