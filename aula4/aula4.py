"""
Tipos de dados
str - String
int - inteiro
float - real/ponto flutuante
bool - booleano
"""
#String: nome
print("Vander", type("Vander"))
#int: idade
print(32, type(32))
#float: altura
print(1.70, type(1.70))
#bool: maior de idade
print(32>18, type(32>18))

# exemplo bool
print('luiz', type('luiz'), bool('luiz'))